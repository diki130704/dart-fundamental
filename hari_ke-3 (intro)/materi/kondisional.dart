void main() {
// OR ( || )
print(true || true); // true
print(true || false); // true
print(true || false || false); // true
print(false || false); // false

// AND ( && )
print(true && true); // true
print(true && false); // false
print(false && false); // false
print(false && true && true); // false
print(true && true && true); // true
}
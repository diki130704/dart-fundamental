void main() {
  var i = 1;
  print("LOOPING PERTAMA");
  while (i <= 20) {
    if (i % 2 == 0) {
      print(i.toString() + " - I love coding");
    } 
    i++;
    }
  var j = 20;
  print("LOOPING KEDUA");
  while (j >= 1) {
    if (j % 2 == 0) {
    print(j.toString() + " - I will become a mobile developer");
    }
    j--;
    }
}